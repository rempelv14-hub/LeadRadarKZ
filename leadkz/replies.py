def template_key_for_category(category: str) -> str:
    lower = (category or "").lower()
    if "курс" in lower or "школ" in lower or "образ" in lower:
        return "courses"
    if "магаз" in lower or "продаж" in lower:
        return "shop"
    if "ресторан" in lower or "доставка" in lower:
        return "restaurant"
    if "услуг" in lower or "запись" in lower:
        return "booking"
    if "crm" in lower or "заяв" in lower or "автомат" in lower:
        return "crm"
    if "сайт" in lower or "лендинг" in lower:
        return "site"
    if "smm" in lower or "реклама" in lower:
        return "smm"
    if "дизайн" in lower:
        return "design"
    return "default"


def render_template(lead: Lead | None, key: str | None = None) -> str:
    chosen = key or template_key_for_category(lead.category if lead else "")
    if chosen == "kp":
        return render_offer(lead)
    if chosen == "questions":
        return render_questions()
    text = TEMPLATES.get(chosen, TEMPLATES["default"])
    if not lead:
        return text
    return (
        f"💬 <b>Шаблон ответа</b>\nКатегория: <b>{lead.category}</b>\n"
        f"Бюджет: <b>{estimate_budget(lead.text, lead.category)}</b>\n\n"
        f"<code>{text}</code>\n\nЛучше отправлять вручную от своего аккаунта, без авторассылки."
    )


def render_questions() -> str:
    return "❓ <b>Что спросить у клиента</b>\n\n" + "\n".join(f"{i}. {q}" for i, q in enumerate(QUESTIONS, start=1))


def render_offer(lead: Lead | None) -> str:
    category = lead.category if lead else "чат-бот / автоматизация"
    budget = estimate_budget(lead.text, lead.category) if lead else "от 30 000 ₸"
    return (
        "📄 <b>Мини-КП для клиента</b>\n\n"
        f"<b>Проект:</b> {category}\n"
        "<b>Что можно сделать:</b>\n"
        "— Telegram-бот/сайт/автоматизация под задачу;\n"
        "— прием заявок или запись клиентов;\n"
        "— уведомления администратору;\n"
        "— база клиентов и статусы;\n"
        "— оплата/Kaspi или CRM-интеграция при необходимости.\n\n"
        "<b>Срок:</b> 3–7 дней для простой версии.\n"
        f"<b>Ориентир по бюджету:</b> {budget}\n\n"
        "Текст можно скопировать и адаптировать под клиента."
    )


